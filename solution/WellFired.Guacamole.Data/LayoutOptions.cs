namespace WellFired.Guacamole.Data
{
	public enum LayoutOptions
	{
	    /// <summary>
	    /// Your view will expand based on the size of it's content.
	    /// </summary>
		Expand,

	    /// <summary>
	    /// Your view will expand to fill it's parent.
	    /// </summary>
		Fill,

	    /// <summary>
	    /// Your view will be centered within it's parent.
	    /// </summary>
	    Center
	}
}